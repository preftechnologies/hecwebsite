  <?php
// Database connection
$servername = "localhost";
$username = "root";
$password = "";
$dbname = "hecwebsite";

$conn = new mysqli($servername, $username, $password, $dbname);

// Check connection
if ($conn->connect_error) {
    die("Connection failed: " . $conn->connect_error);
}

// Pagination Setup
$videosPerPage = 3; // Show 3 videos per page
$page = isset($_GET['page']) ? (int)$_GET['page'] : 1;
$offset = ($page - 1) * $videosPerPage;

// Fetch total video count
$totalVideosResult = $conn->query("SELECT COUNT(*) AS total FROM videos");
$totalVideos = $totalVideosResult->fetch_assoc()['total'];
$totalPages = ceil($totalVideos / $videosPerPage);

// Fetch videos with LIMIT for pagination
$sql = "SELECT id, title, description, video_url, uploaded_at FROM videos ORDER BY uploaded_at DESC LIMIT $videosPerPage OFFSET $offset";
$result = $conn->query($sql);
?>


